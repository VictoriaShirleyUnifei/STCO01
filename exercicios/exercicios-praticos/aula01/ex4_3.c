//Suponha que v é um vetor. Descreva a diferença conceitual entre as expressões  v[3]  e  v + 3.

//Resposta: v[3] é o quarto elemento do vetor v, 
//enquanto v + 3 é o endereço do quarto elemento do vetor v.
//Explicação: v[3] acessa o valor armazenado na posição 3 do vetor v, 
//enquanto v + 3 calcula o endereço de memória dessa posição,
//considerando o tamanho do tipo dos elementos do vetor. 
//Portanto, a diferença conceitual é que v[3] é um valor e v + 3 é um endereço de memória.
