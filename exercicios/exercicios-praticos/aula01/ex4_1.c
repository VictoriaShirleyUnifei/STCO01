//Suponha que os elementos de um vetor v 
//são do tipo int e cada int ocupa 4 bytes no seu computador. 
//Se o endereço de v[0] é 55000, qual o valor da expressão  v + 3?

//Resposta: 55012
//Explicação: v + 3 é o endereço do quarto elemento do vetor v, 
//que ocupa 4 bytes cada, então 55000 + 3 * 4 = 55012.
//Portanto, o valor da expressão v + 3 é 55012.