//Que tipos de problemas podem ser resolvidos por um algoritmo recursivo?

//Algoritmos recursivos são particularmente eficazes 
//para resolver problemas que podem ser divididos em subproblemas 
//menores e semelhantes ao problema original. 
//Alguns tipos comuns de problemas que podem ser resolvidos por algoritmos recursivos incluem:

//1. Problemas de Divisão e Conquista:
//   - Exemplos: Ordenação (como QuickSort e MergeSort),
//     Pesquisa binária, cálculo de potências.
//2. Problemas de Estruturas de Dados Recursivas:
//   - Exemplos: Percorrer árvores (como árvores binárias de busca),
//     manipulação de listas ligadas.
//3. Problemas Matemáticos:
//   - Exemplos: Cálculo de fatorial, sequência de Fibonacci, somatório.
//4. Problemas de Backtracking:
//   - Exemplos: Problemas de combinação e permutação,
//     resolução de labirintos, problemas de coloração de grafos.
//5. Problemas de Grafos:
//   - Exemplos: Busca em profundidade (DFS),
//     busca em largura (BFS) (embora BFS seja geralmente implementado iterativamente
//     pode ser adaptado para recursão).